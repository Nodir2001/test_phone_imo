
function onmouseover_1(over){
    let elem2 = document.getElementById("main_block");
    let elemOver = document.querySelector("#HiddenBlockLengRU");
    
    if(elem2.style.backgroundColor == "rgb(0, 2, 18)"){
        elemOver.style.color = "#fff";
        over.style.backgroundColor="#313036";
    }
    else{
        elemOver.style.color = "#000";
        over.style.backgroundColor="#E6E6E6";
    }
    
    
}
function onmouseout_1_1(out){
    let elem2 = document.getElementById("main_block");
   
    let elemOver = document.querySelector("#HiddenBlockLengRU");
    elemOver.style.color = "#969B9F";
    if(elem2.style.backgroundColor == "rgb(0, 2, 18)"){
        elemOver.style.color = "#969B9F";
        out.style.backgroundColor="#27262C";
    }
    else{
        elemOver.style.color = "#969B9F";
        out.style.backgroundColor="#F5F5F5";
        
    }
}
